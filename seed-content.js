async function seed() {
  console.log('\n📝 Заливаем промпты...');
  for (const p of prompts) {
    await put(`/api/prompts/${encodeURIComponent(p.id)}`, p);
  }

  console.log('\n🔧 Заливаем инструменты...');
  for (const t of tools) {
    await put(`/api/tools/${encodeURIComponent(t.id)}`, t);
  }

  console.log('\n🏢 Заливаем подразделения...');
  // Сначала очищаем через PUT всего массива
  const res = await fetch(`${BASE}/api/departments`, {
    method: 'PUT',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(departments)
  });
  console.log(res.ok ? '  ✅ Подразделения обновлены' : '  ❌ Ошибка подразделений');

  console.log('\n✅ Готово! Перезагрузите страницу.');
}

seed().catch(console.error);

