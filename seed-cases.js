// Заливаем все кейсы через API
async function seed() {
  const BASE = 'http://localhost:19080';
  for (const c of cases) {
    const res = await fetch(`${BASE}/api/cases/${encodeURIComponent(c.id)}`, {
      method: 'PUT',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(c)
    });
    const data = await res.json();
    console.log(res.ok ? `✅ ${c.id}` : `❌ ${c.id}: ${data.error}`);
  }
  console.log('Done!');
}

seed().catch(console.error);

